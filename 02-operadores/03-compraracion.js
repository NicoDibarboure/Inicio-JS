let a = 10;
//Operadores relacionales
console.log(a > 5);
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);

//Operadores de igualdad
console.log(a == 5); // Es igual a 5
console.log(a === 5); // Es igual a 5 y del mismo tipo
console.log(a != 10); // No es igual a 10
console.log(a != 5); // No es igual a 5
console.log(a !== 5); // No es igual a 5 y del mismo tipo
