// Declaracion de Funcion (Function Declaration)

function sumar(a, b) {
  console.log(a + b);
}

// Llamado a la funcion con los parametros.
sumar(2, 3);
sumar(200, 184);
sumar(342, 42);

// Expression de Funcion (Function Expression)

const sumar2 = function () {
  console.log(3 + 3);
};

sumar2();
